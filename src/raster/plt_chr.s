;
; PURPOSE: Plots a single character, as a bitmap from a font table, to the screen.
;
; INPUT: Address(UINT8*): to the start of the screen
;        Position(row,col): the coordinates of the top left pixel of the character
;        ch(char): the character to be written to the screen
;
; OUTPUT: None
;
; void plot_character(UINT8 *base, UINT16 row, UINT16 col, char ch);
;________________________________________________________________
; Plot a single ASCII character using hardcoded 8x16 font.
;
; I couldn't get the built-in Atari library to work, so "fuck it" was declared.
;
; ---------------------------------------------------------------------------------------------------
; FONT SOURCE: The 8x16 bitmap font data is based on the IBM VGA 8×16 character set,
;              as implemented in the Linux kernel (drivers/video/console/font_8x16.c).
;              Reference: Linux kernel source tree, lib/fonts/font_8x16.c
;
;          Fid it here: https://github.com/torvalds/linux/blob/master/lib/fonts/font_8x16.c
; ------------------------------------------------------------------------------------------------------
        xdef    _plot_character
        xref    _plot_bitmap_8

base    equ     8                               ; offset from A6
row     equ     12              
col     equ     14
ch      equ     16                              ; character (extended to word)

FONT_HEIGHT equ 16                              ; 8x16 font
CHAR_BYTES equ  16                              ; 16 bytes per character


_plot_character:
        link    a6,#0
        movem.l d0-d3/a0-a1,-(sp)

check_char_bounds:
                ; Get character code and calculate font offset
        move.w  ch(a6),d0                       ; get character code
                
                ; Bounds check: ensure character is in range 32-127 (printable ASCII)
        cmpi.w  #32,d0
        blo     done                            ; if < 32, skip (control character)
        cmpi.w  #127,d0
        bhi     done                            ; if > 127, skip (extended ASCII)
                
calculate_font_offset:
                ; Subtract offset since font table starts at ASCII 32
        subi.w  #32,d0                          ; d0 = character - 32
        ext.l   d0
        mulu.w  #CHAR_BYTES,d0                  ; multiply by 16 bytes per character
                
                ; Get pointer to character bitmap in font table
        lea     font_data,a1                    ; a1 = base of font table
        adda.l  d0,a1                           ; a1 = pointer to character bitmap
                
draw_character:
                ; Call plot_bitmap_8(base, row, col, character_bitmap, 16)
        move.w  #FONT_HEIGHT,-(sp)              ; push height (16)
        move.l  a1,-(sp)                        ; push font character address
        move.w  col(a6),-(sp)                   ; push col
        move.w  row(a6),-(sp)                   ; push row
        move.l  base(a6),-(sp)                  ; push base
                
        jsr     _plot_bitmap_8
                
        lea     14(sp),sp                       ; clean up parameters
                
done:
        movem.l (sp)+,d0-d3/a0-a1
        unlk    a6
        rts

